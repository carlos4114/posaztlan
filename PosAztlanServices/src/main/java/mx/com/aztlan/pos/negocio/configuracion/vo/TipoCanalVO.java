package mx.com.aztlan.pos.negocio.configuracion.vo;

public class TipoCanalVO {
	private Integer idTipoCanal;
	private String nombre;
	private String descripcion;
	

	public Integer getIdTipoCanal() {
		return idTipoCanal;
	}
	public void setIdTipoCanal(Integer idTipoCanal) {
		this.idTipoCanal = idTipoCanal;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	

}
